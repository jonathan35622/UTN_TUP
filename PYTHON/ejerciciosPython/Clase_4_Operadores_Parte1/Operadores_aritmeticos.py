operandoA = 8;
operandoB = 5;

suma = operandoA + operandoB;
print("Resultado de la suma es: ", suma)

#interpolación
interpolacion = "Estoy imprimiendo por interpolación. La interpolación es incluir una variable en una cadena"
print(f'El resultado de la suma es: {suma}')
print(f'{interpolacion}')

resta = operandoA - operandoB
print(f"El resultado de la resta es {resta}")

multiplicacion = operandoA * operandoB
print(f"El resultado de la multiplicación es {multiplicacion}")

division = operandoA / operandoB
print(f"El resultado de la división es: {division}")

modulo = operandoA % operandoB
print(f"el resultado de la división o residuo (modulo) es: {modulo}")

exponente = operandoA ** operandoB
print(f"El resultado del exponente es: {exponente}")