#comparamos si son iguales- OPERADOR IGUALDAD
d = 4
b = 4
resultado = d == b

print(f"El resultado es {resultado}")

#comparamos si son diferentes- OPERADOR DIFERENTE
resultado = d != b
print(f"El resultado operador diferente {resultado}")

#Operador mayor que
resultado = d > b
print(f"El resultado operador mayor que es:  {resultado}")

#Operadpr menor que
resultado = d < b
print(f"El resultado del operador menor que es:  {resultado}")

#Operador menor o igual que
resultado = d <= b
print(f"El resultado del operador menor o igual que es {resultado}")

#Operador mayor o igual que
resultado = d >= b
print(f"El resultado del operador mayor o igual que es {resultado}")